# replace function
crazy = "The!quick!brown!fox!jumps!over!the!lazy!dog!."
print("Given String: ",crazy)
crazy_re_place = crazy.replace("!"," ")
print("Replacing '!' with balnk space: {}".format(crazy_re_place))
print("In Upper Case: {} ".format(crazy_re_place.upper()))
print("In Reverse: {} ".format(crazy_re_place[::-1]))